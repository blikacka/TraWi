package com.tracker;

/**
 *
 * @author Notasek
 */
public class FilesSmsSetter {

    public String smsFormat;
    public String smsAlias;
    public int smsNumber;

    public String getSmsFormat() {
        return smsFormat;
    }

    public void setSmsFormat(String smsFormat) {
        this.smsFormat = smsFormat;
    }

    public String getSmsAlias() {
        return smsAlias;
    }

    public void setSmsAlias(String smsAlias) {
        this.smsAlias = smsAlias;
    }

    public int getSmsNumber() {
        return smsNumber;
    }

    public void setSmsNumber(int smsNumber) {
        this.smsNumber = smsNumber;
    }
}
