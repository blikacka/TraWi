public class SmsFormatSetter {

    public NumbersSetter numbersSetter;
    public String smsFormat;
    public String alias;

    public NumbersSetter getNumbersSetter() {
        return numbersSetter;
    }

    public void setNumbersSetter(NumbersSetter numbersSetter) {
        this.numbersSetter = numbersSetter;
    }

    public String getSmsFormat() {
        return smsFormat;
    }

    public void setSmsFormat(String smsFormat) {
        this.smsFormat = smsFormat;
    }

    public String getAlias() {
        return alias;
    }

    public void setAlias(String alias) {
        this.alias = alias;
    }


    
}
